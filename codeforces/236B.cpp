// LINK: https://codeforces.com/problemset/problem/236/B

#include <bits/stdc++.h>
using namespace std;
#define pb push_back
#define MAX 1e9+7

int main() {
  int a, b, c;
  const int M = 1073741824;
  cin >> a >> b >> c;
  // int mx = max(max(a, b), c);
  //cout << mx;
  int mx = a*b*c;
  vector<int> div(mx+1, 0);
  for (int i = 1; i <= mx; i++) { // counting divisors
    for (int j = i; j <= mx; j += i) {
      div[j]++;
    }
  }
  int sum = 0; 
  for (int i = 1; i <= a; i++) {
    for (int j = 1; j <= b; j++) {
      for (int k = 1; k<= c; k++) {
        sum += div[i*j*k] % M;
      }
    }
  }

  cout << sum << '\n';


  return 0;
}
